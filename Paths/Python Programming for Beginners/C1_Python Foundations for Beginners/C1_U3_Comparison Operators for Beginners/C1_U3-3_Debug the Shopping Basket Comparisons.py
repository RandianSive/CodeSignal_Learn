# Scenario: We have some cupcakes and candies in our shopping basket.
cupcakes = 5
candies = 3

# Calculate if the number of cupcakes is greater than the number of candies.
more_cupcakes = cupcakes > candies
print(more_cupcakes)  # Expected to print: True

# Compare using operator chaining to check if cupcakes are less than 6 and more than 3
buy_more_cupcakes = 6 > cupcakes > 3
print(buy_more_cupcakes)  # Expected to print: True