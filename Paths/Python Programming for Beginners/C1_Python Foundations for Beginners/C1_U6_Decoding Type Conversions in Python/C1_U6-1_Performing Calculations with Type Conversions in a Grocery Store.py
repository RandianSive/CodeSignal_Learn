# You are at a grocery store, and you have the price of apples in string form
price = "10"

# To perform some calculations, you need to convert the price into an integer
price_int = int(price)

# Now, you can easily perform arithmetic operations
total_amount = price_int * 5
print(total_amount)  # Outputs: 50

print("Total cost of apples: $", total_amount)  # Outputs: Total cost of apples: $ 50