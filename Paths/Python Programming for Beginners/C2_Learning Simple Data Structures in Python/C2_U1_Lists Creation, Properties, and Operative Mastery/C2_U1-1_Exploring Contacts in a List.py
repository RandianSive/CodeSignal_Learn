# Our initial contact list
contact_list = ["Tom", "Jerry", "Spike", "Tyke"]

# Let's see the size of our list
print(len(contact_list))  # Outputs: 4

# Let's see the 2nd contact in our list
print(contact_list[1])  # Outputs: 'Jerry'

# Let's see the last contact in our list using a negative index
print(contact_list[-1])  # Outputs: 'Tyke'